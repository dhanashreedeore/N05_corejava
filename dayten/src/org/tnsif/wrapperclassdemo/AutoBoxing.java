package org.tnsif.wrapperclassdemo;
/*program to demonstrate on auto-boxing
 * AutoBoxing : Conversion of primitive type to object type*/
public class AutoBoxing {

	public static void main(String[] args) {
		char c='A';
		//converting primitive data type into object type
		Character ch=c;
		System.out.println(ch);
	}

}
